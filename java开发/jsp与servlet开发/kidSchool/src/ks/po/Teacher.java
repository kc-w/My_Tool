package ks.po;
//教师类
public class Teacher {
    //教师id
    private Integer id;
    //教师姓名
    private String name;


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }
}
